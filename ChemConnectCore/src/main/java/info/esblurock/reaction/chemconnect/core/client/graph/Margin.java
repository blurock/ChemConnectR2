package info.esblurock.reaction.chemconnect.core.client.graph;
public class Margin {
	public final int top;
	public final int right;
	public final int bottom;
	public final int left;

	public Margin(final int top, final int right, final int bottom,
			final int left) {
		super();
		this.top = top;
		this.right = right;
		this.bottom = bottom;
		this.left = left;
	}
}